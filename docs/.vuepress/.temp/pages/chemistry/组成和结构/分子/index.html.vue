<h2 id="化学变化的实质" tabindex="-1"><a class="header-anchor" href="#化学变化的实质"><span>化学变化的实质</span></a></h2>
<p>由分子构成的物质在发生化学变化时，<mark>分子分成原子，原子又重新结合成新分子</mark>。即发生化学变化时，分子破裂、原子重组。</p>
<p>如氢气在氧气中燃烧生成水的表达式：H<sub>2</sub> + O<sub>2</sub> --&gt; H<sub>2</sub>O</p>
<p>在化学反应前后，分子的种类一定发生变化，分子的数目可能改变，也可能不变；原子的种类和数目一定不变。</p>
