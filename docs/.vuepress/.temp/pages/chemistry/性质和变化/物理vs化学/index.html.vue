<h2 id="物理性质和化学性质-⭐⭐⭐" tabindex="-1"><a class="header-anchor" href="#物理性质和化学性质-⭐⭐⭐"><span>物理性质和化学性质 ⭐⭐⭐</span></a></h2>
<VPCardGrid><VPCard title="物理性质" icon="hugeicons:physics"><p><code v-pre>概念</code>：<mark>物质不需要发生化学变化就表现出来的性质</mark></p>
<p><code v-pre>特点</code>: 构成物质的基本粒子（如分子）集体才能表现出来。</p>
<p><code v-pre>举例</code>：颜色、状态、气味、熔点、沸点、硬度、密度、挥发性、溶解性、吸附性、导电性、导热性、延展性等</p>
</VPCard><VPCard title="化学变化" icon="hugeicons:chemistry-01"><p><code v-pre>概念</code>：<mark>物质在化学变化中表现出来的性质</mark></p>
<p><code v-pre>特点</code>: 构成物质的单个粒子（如分子、原子、离子）就能表现出来。</p>
<p><code v-pre>举例</code>：可燃性、助燃性、稳定性、还原性、氧化性、腐蚀性、酸碱性、毒性等。</p>
</VPCard></VPCardGrid><p><code v-pre>根本区别</code>：<mark>是否需要通过化学变化表现出来</mark>。</p>
