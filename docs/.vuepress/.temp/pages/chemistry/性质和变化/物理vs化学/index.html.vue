<VPCardGrid><VPCard title="物理变化" icon="hugeicons:physics"><p><code v-pre>定义</code>：没有生成新物质的变化</p>
<p><code v-pre>特征</code>: <mark>无新物质生成</mark></p>
<p><code v-pre>举例</code>：蜡烛的熔化、酒精的挥发、铁水铸成锅</p>
<p><code v-pre>微观实质</code>：分子本身没有发生变化</p>
</VPCard><VPCard title="化学变化" icon="hugeicons:chemistry-01"><p><code v-pre>定义</code>：生成新物质的变化（又叫<mark>化学反应</mark>）</p>
<p><code v-pre>特征</code>: <mark>生成新物质</mark></p>
<p><code v-pre>举例</code>：蜡烛的燃烧、铁生锈、食物变质</p>
<p><code v-pre>微观实质</code>：分子破裂成原子，再重组为新分子。</p>
</VPCard></VPCardGrid><p><code v-pre>本质区别</code>：<mark>变化时是否有新物质产生</mark>。</p>
<p><code v-pre>相互联系</code>：<mark>在发生化学变化的过程中一定伴随着物理变化</mark>，而发生物理变化时不一定同时发生化学变化。</p>
<div class="hint-container warning">
<p class="hint-container-title">爆炸可能是物理变化，也可能是化学变化。</p>
<p>如轮胎“爆胎”、蒸汽锅炉因压力过大而发生的爆炸，这些过程中无新物质生成，发生的是物理变化；而火药爆炸、瓦斯爆炸，这些变化过程中生成了新物质，发生的是化学变化。</p>
