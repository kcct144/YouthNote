<h2 id="三、特殊情况" tabindex="-1"><a class="header-anchor" href="#三、特殊情况"><span>三、特殊情况</span></a></h2>
<p>如果句子中有 if，遵循“<mark>主过从过，主将从现</mark>”原则。即 if 引导的条件状语从句用一般现在时，主句用将来时。</p>
<p>I will tell him <mark>if</mark> he ____ back.</p>
<p>A. come B. comes C. will come</p>
<p>在这题中，主句为 I will tell him，从句为 if he comes back。主将从现，选 B。</p>
