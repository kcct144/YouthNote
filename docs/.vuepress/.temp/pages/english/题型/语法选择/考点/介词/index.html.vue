<template><div><h2 id="考点判断" tabindex="-1"><a class="header-anchor" href="#考点判断"><span>考点判断</span></a></h2>
<p>英语中有一类虚词，称为介词。以下词都是介词。</p>
<p>in, at, on, of, to, for, with, by, from, into, under, in front of, behind, before, after, like, except...</p>
<p>介词通常和后面的宾语一同构成介词短语，用来表示时间、地点、方式等各种信息。</p>
<ul>
<li>in the morning 在早上</li>
<li>after school 放学后</li>
<li>like my father 像我父亲一样</li>
</ul>
<p>如果 A、B、C 三个选项都是介词，则考点为介词。</p>
<h2 id="思路总结" tabindex="-1"><a class="header-anchor" href="#思路总结"><span>思路总结</span></a></h2>
<p>介词的主要思路有两个：</p>
<ul>
<li>搭配</li>
<li>一般用法</li>
</ul>
<ol>
<li>I enjoy talking with my classmates_______our way to school.</li>
</ol>
<p>A. on 	B. at 	C. in 	D. under</p>
<p>在这一题中，根据搭配 on one's way to... （在某人去...的路上）可知，此题选A。</p>
<ol start="2">
<li>He goes to school ________ bike.</li>
</ol>
<p>A. on 	B. for 	C. in 	D. by</p>
<p>表示乘坐某种交通工具，介词用 by，这题选 D。</p>
