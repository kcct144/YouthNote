<h2 id="一、讲解" tabindex="-1"><a class="header-anchor" href="#一、讲解"><span>一、讲解</span></a></h2>
<p>多个事物之间进行比较，分为三种情况：</p>
<ol>
<li>原级比较（一样...；都...）</li>
<li>比较级（更...）</li>
<li>最高级（最...）</li>
</ol>
