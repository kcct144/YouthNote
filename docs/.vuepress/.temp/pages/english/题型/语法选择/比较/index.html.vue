<h3 id="_2-比较级" tabindex="-1"><a class="header-anchor" href="#_2-比较级"><span>2. 比较级</span></a></h3>
<p>需要表示“更...”的时候，会用到比较级。</p>
<Tabs id="167" :data='[{"id":"构成方式"},{"id":"用法"}]'>
<template #title0="{ value, isActive }">构成方式</template><template #title1="{ value, isActive }">用法</template><template #tab0="{ value, isActive }"><p>比较级的一般构成有两种：</p>
<ol>
<li>在形容词或副词<mark>后面加 -er</mark> （单音节词和部分双音节词）</li>
</ol>
<ul>
<li>smart → smarter（更聪明）</li>
<li>strong → stronger（更强壮）</li>
<li>tall → taller（更高）</li>
</ul>
<ol start="2">
<li>在形容词或副词<mark>前面加 more</mark> （多音节词和部分双音节词）</li>
</ol>
<ul>
<li>beautiful → more beautiful（更漂亮）</li>
<li>interesting → more interesting（更有趣）</li>
<li>quickly → more quickly（更快）</li>
</ul>
</template><template #tab1="{ value, isActive }"><p><mark class="warning">含有“than（比...）”的时候，一般都会用比较级</mark>：</p>
