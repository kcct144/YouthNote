<p>物主代词分为两种：形容词性物主代词和名词性物主代词。两者都表示“...的”。</p>
<ul>
<li>形容词性物主代词是<mark>限定词</mark>，用来限定名词，构成名词短语，因此<mark>后面常常接着一个名词</mark></li>
<li>名词性物主代词是<mark>代词</mark>，用来代指“...的东西”，相当于“形物代+名词”，因此<mark>后面一般不接名词</mark>。</li>
</ul>
<p>例如：</p>
<ul>
<li>This is <mark>my</mark> book. 这是<mark>我的</mark>书。</li>
<li>This book is <mark>mine</mark>. 这本书是<mark>我的（书）</mark>。（mine = my book）</li>
</ul>
