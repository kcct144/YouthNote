</template></Tabs><h3 id="_3、反身代词" tabindex="-1"><a class="header-anchor" href="#_3、反身代词"><span>3、反身代词</span></a></h3>
<p>反身代词是一种表示反射或强调的代词，它由第一人称、第二人称和第三人称的形容词性物主代词加上后缀“-self”（单数）或“-selves”（复数）构成。</p>
<ol>
<li>第一人称：myself（我自己）、ourselves（我们自己）</li>
<li>第二人称：yourself（你自己）、yourselves（你们自己）</li>
<li>第三人称：himself（他自己）、herself（她自己）、itself（它自己）、themselves（他们/她们/它们自己）</li>
</ol>
<p>反身代词的三大用法：</p>
<ol>
<li>
<p>用作宾语，表示动作的承受者就是主语本身。（<mark>主语和宾语一致</mark>）</p>
<ul>
<li>I teach <mark>myself</mark> English. 我教我自己英语。（我自学英语）</li>
</ul>
</li>
<li>
<p>用作同位语，表示强调。（<mark>亲自、自己一个人、自己</mark>）</p>
<ul>
<li>She <mark>herself</mark> will go to the party. 她自己将去参加派对。</li>
</ul>
</li>
<li>
<p>构成短语搭配。（<mark class="warning">常用 oneself 在搭配中表示反身代词</mark>）</p>
<ul>
<li>by <mark>oneself</mark> 独自</li>
<li>enjoy <mark>oneself</mark> 玩得开心</li>
</ul>
</li>
</ol>
