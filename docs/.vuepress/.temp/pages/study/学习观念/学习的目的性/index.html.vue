</ul>
</li>
<li>200个高频词背诵（进步5分）
<ul>
<li>每日 20 个</li>
</ul>
</li>
<li>背诵作文模板（5分→10分+）</li>
</ul>
</li>
<li>语文 10 分
<ul>
