<li>解决分式化简问题 （3分）</li>
</ul>
</li>
<li>...</li>
</ul>
</li>
</ul>
<p><code v-pre>拆解</code> 和 <code v-pre>量化</code> 的思维之所以重要，是因为它能将一个混沌的（不知道自己怎么进步的）学习过程，转化为清晰的成长地图。</p>
<p>就像一个待办清单一样。自己在解决什么问题？需要学习哪些东西？如何学习？学习到了什么程度？大概能拿到多少分？都是非常清晰的。中间不存在“不知道做什么”或“不知道为什么进步/退步”的问题。</p>
