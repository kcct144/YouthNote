<h2 id="_1-我要解决什么问题" tabindex="-1"><a class="header-anchor" href="#_1-我要解决什么问题"><span>1. 我要解决什么问题？</span></a></h2>
<p><mark>所有的学习行为的根源，都应该指向一个具体的问题</mark>。这个问题可以是<code v-pre>长期的</code>，如考一所好高中；也可以是<code v-pre>短期的</code>，如鸡兔同笼问题。</p>
<p>如果不知道自己有什么要解决的问题，那就先去做题，或者看每次考试的错题，看看自己不会的题目，以及对应的答案解析，<mark>从错误中总结问题</mark>。</p>
<h2 id="_2-需要学习什么" tabindex="-1"><a class="header-anchor" href="#_2-需要学习什么"><span>2. 需要学习什么？</span></a></h2>
<p>每个问题的背后都隐藏着一套知识体系。<mark>任何要解决的问题、学习的东西，都是可以分析、拆解的</mark>。要解决问题，就要抽丝剥茧，找到和问题直接相关的关键线索。</p>
<p>例如：如何提高英语听力？</p>
<ul>
<li>核心知识：语音规则、高频词汇、语境理解...</li>
<li>可能方法：精听、泛听、听写、背诵...</li>
<li>无关的内容：书写、生僻字的拼写...</li>
</ul>
<p>学习不是学的越多越好。学的多，同时意味着花费的时间和精力多，而我们的时间精力又是有限的。<mark>知识的价值不在于数量，而在于能否解决问题</mark>。因此我们要多去区分重点、次重点、非重点。例如数学考试中优先掌握基础公式，而非那些偏题怪题。学习的时候也不要被次重点和非重点的内容吓到丧失信心。</p>
<h2 id="_3-需要学到什么程度" tabindex="-1"><a class="header-anchor" href="#_3-需要学到什么程度"><span>3. 需要学到什么程度？</span></a></h2>
<p>很多学生只知道说“我不会”，“我听不懂”，“我大概懂了”，“差不多会了”等言语来描述自己的学习进度，这种模糊的认识导致学习非常低效且重复。<mark>要学会从“模糊”的思考转化为“量化”的思考</mark>。</p>
<p>学习新知识、新概念的时候，给自己一些评估标准，例如：</p>
<ul>
<li>我们不能复述核心概念？（什么是牛顿第一定律？）</li>
<li>我能否解决这类问题？（学习三角函数知识点后，能不能独立解决作业里的问题？）</li>
<li>我能不能举出一些具体例子？（能否说出几个介词？）</li>
</ul>
<p>较为长期的学习，也可以为自己建立一些阶段目标：</p>
<ul>
<li>下次段考总分进步100分
