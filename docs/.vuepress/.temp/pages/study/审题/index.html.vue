<h2 id="二、无效审题" tabindex="-1"><a class="header-anchor" href="#二、无效审题"><span>二、无效审题</span></a></h2>
<h3 id="_1-我以为我会-——靠直觉判断-不靠认真看题​" tabindex="-1"><a class="header-anchor" href="#_1-我以为我会-——靠直觉判断-不靠认真看题​"><span>1. “我以为我会”——靠直觉判断，不靠认真看题​</span></a></h3>
<p>表现​​：考试/作业时，看到题目第一反应不是仔细读题，而是拍脑袋：“这题我学过！”“老师讲过类似的！”“肯定不难！”然后直接动手做，结果要么卡壳，要么错得离谱。做完题对答案，发现错得莫名其妙，却想不通“明明我会啊，怎么错了？”或者面对自己很可能会的题，不去分析，不去拆解，而是直接说“我肯定不会”，白白错失了得高分的机会。</p>
<h3 id="_2-我看了-但没懂-——只看问题-不挖逻辑​" tabindex="-1"><a class="header-anchor" href="#_2-我看了-但没懂-——只看问题-不挖逻辑​"><span>2. “我看了，但没懂”——只看问题，不挖逻辑​</span></a></h3>
<p>表现​​：审题时眼睛快速扫过题目，只盯着“问什么”（比如“求什么”“答什么”），却不理清楚“已知条件和答案之间的联系”。就像拼图只看最后一块，却不知道前面的碎片怎么拼，结果思路乱成一团。审题不是“看问题猜答案”，而是“拆条件找逻辑”——题目中的每个细节都是解题的关键，不挖清楚，答案就会“缺胳膊少腿”。</p>
<h3 id="_3-我有知识点-但不会用-——只抓碎片-不会整合​" tabindex="-1"><a class="header-anchor" href="#_3-我有知识点-但不会用-——只抓碎片-不会整合​"><span>3. “我有知识点，但不会用”——只抓碎片，不会整合​</span></a></h3>
<p>表现​​：审题时能找到几个“熟悉的关键词”（比如“浮力”“欧姆定律”“光合作用”），但不会把这些知识点和题目中的具体问题连起来，就像手里有很多拼图块，却不知道怎么拼成完整的图。审题不是“挑熟悉的题”，而是“串知识的线”——题目中的每个条件（比如“串联”“中点”“电流表示数”）都需要和学过的知识点（欧姆定律、滑动变阻器原理）连起来，否则知识点再多也白搭！</p>
